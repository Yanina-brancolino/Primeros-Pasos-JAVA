public class ImpresionEnPantalla {
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, World!");
        System.out.println("Me amo!"); 
        
    }
}

// Escribiendo sout ===> System.out.println() podemos imprimir en pantalla 
// (seria la misma funcionalidad que el console.log() de JavaScript).

// Otro dato a tener en cuenta es que al final de cada linea de codigo, 
// se debe de colocar ===> ;
// ya que JAVA tiene un tipado statico y un compilador estricto.
// A diferencia de JavaScript que tiene un tipado dinamico y un compilador adaptable al momento de ejecucion.