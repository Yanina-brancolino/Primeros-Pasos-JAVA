public class VariablesTiposDeDatos {
    public static void main(String[] args) {

        int numero = 5;
        String nombre = "Vitto";
        char letra = 'A';
        double temperatura = 11.2;
        boolean siONo = false;
        long largo = 123456789;
    }
}

/* 
    Los Tipos De Datos Son:

    int ---> para ENTEROS 
    double ---> para DECIMALES
    boolean ---> para TRUE O FALSE
    char ---> para 1 CARACTER 
    String ---> para un ARRAY DE CARACTERES (texto)
    long ---> para un ENTERO LARGO 
*/

/*  
    Forma correcta para declarar una VARIABLE
    
    se debe declarar al principio el tipo de dato que va a contener ese espacio en memoria,
    luego el nombre que se le valla a dar para referenciar al mismo (lo mas claro y descriptivo posible),
    luego utilizamos el = para asignarle a la variable el dato.
    
    EJEMPLO ===>  int numero = 5;
*/
